export function getStockInfo(ticker: string) {
  const upperTicker = ticker.toUpperCase();
  return STOCK_DATABASE[upperTicker] || null;
}

export function searchStocks(query: string): string[] {
  const upperQuery = query.toUpperCase();
  return Object.keys(STOCK_DATABASE).filter(
    ticker => 
      ticker.includes(upperQuery) || 
      STOCK_DATABASE[ticker].name.toUpperCase().includes(upperQuery)
  ).slice(0, 10);
}
